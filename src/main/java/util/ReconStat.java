package util;

import util.build.ReconStatBuilder;

public class ReconStat {

	private RECON_FIELDS field;
	private int count;
	private int avg;
	private int max;
	private int min;
	
	public ReconStat(ReconStatBuilder b) {
		this.field = b.field;
		this.count = b.count;
		this.avg = b.avg;
		this.max = b.max;
		this.min = b.min;
	} 
	
	public RECON_FIELDS getField() {
		return field;
	}

	public int getCount() {
		return count;
	}

	public int getAvg() {
		return avg;
	}

	public int getMax() {
		return max;
	}

	public int getMin() {
		return min;
	}
}
